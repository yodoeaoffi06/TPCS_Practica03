    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableProductos.getSelectedRow();
        TableModel model = tableProductos.getModel();

        if (selectedRow != -1) {
            producto = new Producto();
            producto.setId_producto((long) model.getValueAt(selectedRow, 0));
            producto.setDescripcion((String) model.getValueAt(selectedRow, 1));
            producto.setPrecio((BigDecimal) model.getValueAt(selectedRow, 2));
            producto.setExistencia((int) model.getValueAt(selectedRow, 3));
            producto.setPrecio((BigDecimal) model.getValueAt(selectedRow, 4));

            JOptionPane.showMessageDialog(null, "El producto " + producto.getDescripcion() + " se ha agregado a la venta.");
            
            callback.onVariableReceived(producto);
            
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "No hay ninguna fila seleccionada.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

